package arithmatic;

public class ArithOperations {

	public static void main(String[] args) 
	{
		int n1=12,n2=13;
		int sum=n1+n2;
		System.out.println("Sum is: " + sum);

		int subs=n1-n2;
		System.out.println("Difference is : " +subs);
		int mult=n1*n2;
		System.out.println("Multiplied value is : " + mult);
		int div=n1/n2;
		System.out.println("Quotient is : " + div);
		int rem = n1 % n2;
		  System.out.println("Remainder is : " + rem);

		
	}

}
