package forloop;

public class Forloop5 {

	public static void main(String[] args) {
		System.out.println("Pyramid  1\n \n ");
		for(int i=1;i<=10;i++)
		{for(int j=1;j<=i;j++) 
		{
			System.out.print( j+" ");
		}
		System.out.println(" ");
		}
		System.out.println("\n \n Pyramid    2 \n \n");
		for(int i=1;i<=10;i++)
		{for(int j=i;j<=10;j++) 
		{
			System.out.print( j+" ");
		}
		System.out.println(" ");
		}
		System.out.println("\n \n  Pyramid   3 \n \n ");
		for(int i=1;i<=10;i++)
		{for(int j=1;j<=i;j++) 
		{
			System.out.print( j+" ");
		}
		System.out.println(" ");
		}
		for(int i=1;i<=10;i++)
		{for(int j=i;j<=10;j++) 
		{
			System.out.print( j+" ");
		}
		System.out.println(" ");
		}
	}

}
