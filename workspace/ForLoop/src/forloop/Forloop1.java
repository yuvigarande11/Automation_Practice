package forloop;

public class Forloop1 {

	public static void main(String[] args) {
		System.out.println("in contineous order");
		for(int i=0;i<=10;i++)
		{System.out.println(+i);
			
		}
		System.out.println("in reverse order");
		for(int i=10;i>=0;i--)
		{System.out.println(+i);
		
		}
		
	}

}
