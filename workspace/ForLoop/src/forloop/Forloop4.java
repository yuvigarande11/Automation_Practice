package forloop;

public class Forloop4 {

	public static void main(String[] args) {
		System.out.println("in contineous even  order");
		for(int i=0;i<=10;i++)
		{System.out.println(i+ "\t");
		i=i+1;
		
		}
		
		System.out.println("in contineous odd order");
		for(int i=1;i<=10;i++)
		{System.out.println(i+ "\t");
			i=i+1;
		}
		
	}

}
